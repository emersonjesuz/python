for c in range(0,6):    # range (0,6) range nunca conta com o ultimo numero
    print('oi')           # então ele para no 5
print('fim')

for c in range(0,6):
    print('oi')           # oque tive dentro do laço vai repetir varias vezes de acordo com a range ( )
    print('fim ')

for c in range(1,7):
    print(c)            # se colocar o "c" no print ele vai contar o numeros da range !

for c in range(6,0,-1):
    print(c)                 # para inverter o laço  coloque na renge -1  range ( 6, 0, -1)
         # assim ele vai inverter e começa a contar alcontrario, sendo assim o zero nao conta

for c in range(0 , 7 , 2):
    print(c)              #  colocando 2 ele vai contar de dois em dois numeros
